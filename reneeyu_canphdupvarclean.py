def is_simplified_or_variant(word):
    """
    判斷詞語是否包含簡體或異體字
    """
    variant_map = build_variant_map()
    for char in word:
        if char in variant_map:
            return True
    return False

def convert_to_traditional(word):
    """
    將詞語中的簡體/異體字轉換為對應的繁體字
    """
    variant_map = build_variant_map()
    result = ''
    for char in word:
        if char in variant_map:
            result += variant_map[char]
        else:
            result += char
    return result

def clean_code_table(input_file, output_file):
    """
    清理碼表，刪除因繁簡/異體造成的重碼詞，保留繁體版本
    """
    # 用字典存儲每個輸入碼對應的詞語列表
    code_dict = {}
    
    # 讀取原始碼表
    with open(input_file, 'r', encoding='utf-16') as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            
            parts = re.split(r'\s+', line)
            if len(parts) >= 2:
                code = parts[0]
                word = ' '.join(parts[1:])
                
                # 只處理雙字詞
                if len(word) == 2:
                    if code not in code_dict:
                        code_dict[code] = []
                    code_dict[code].append(word)
    
    # 找出需要刪除的詞語
    words_to_remove = set()
    
    for code, words in code_dict.items():
        if len(words) <= 1:
            continue
        
        # 對每個詞語，檢查是否有對應的繁體版本
        for i, word1 in enumerate(words):
            for j, word2 in enumerate(words):
                if i >= j:
                    continue
                
                # 檢查是否為繁簡/異體關係
                traditional1 = convert_to_traditional(word1)
                traditional2 = convert_to_traditional(word2)
                
                # 如果轉換後的繁體字相同，則刪除簡體/異體版本
                if traditional1 == traditional2:
                    # 保留繁體版本，標記簡體/異體版本為待刪除
                    if is_simplified_or_variant(word1):
                        words_to_remove.add((code, word1))
                    if is_simplified_or_variant(word2):
                        words_to_remove.add((code, word2))

    # 寫入清理後的碼表
    removed_count = 0
    kept_count = 0
    
    with open(input_file, 'r', encoding='utf-16') as f_in:
        with open(output_file, 'w', encoding='utf-16') as f_out:
            f_out.write('\ufeff')
            
            for line in f_in:
                line = line.strip()
                if not line:
                    f_out.write('\n')
                    continue
                
                parts = re.split(r'\s+', line)
                if len(parts) >= 2:
                    code = parts[0]
                    word = ' '.join(parts[1:])
                    
                    # 檢查是否為需要刪除的詞語
                    if (code, word) in words_to_remove:
                        removed_count += 1
                        continue
                
                f_out.write(line + '\n')
                kept_count += 1
    
    print(f"清理完成！")
    print(f"保留詞條: {kept_count}")
    print(f"刪除詞條: {removed_count}")
    print(f"結果已輸出到 {output_file}")

