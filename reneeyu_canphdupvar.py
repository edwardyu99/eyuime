def is_variant_related(word1, word2):
    """
    判斷兩個詞語是否因繁簡/異體關係而重碼
    檢查兩個詞語是否在對應位置有繁簡/異體關係
    """
    if len(word1) != 2 or len(word2) != 2:
        return False
    
    # 檢查是否其中一個字的繁簡體對應
    # 情況1: 第一個字相同，第二個字是繁簡/異體關係
    if word1[0] == word2[0]:
        if word1[1] in variant_map and variant_map[word1[1]] == word2[1]:
            return True
        if word2[1] in variant_map and variant_map[word2[1]] == word1[1]:
            return True
    
    # 情況2: 第二個字相同，第一個字是繁簡/異體關係
    if word1[1] == word2[1]:
        if word1[0] in variant_map and variant_map[word1[0]] == word2[0]:
            return True
        if word2[0] in variant_map and variant_map[word2[0]] == word1[0]:
            return True
    
    # 情況3: 兩個字都不同，但可能都是繁簡/異體關係
    # (這種情況較少見，但也要檢查)
    if (word1[0] in variant_map and variant_map[word1[0]] == word2[0] or
        word2[0] in variant_map and variant_map[word2[0]] == word1[0]):
        if (word1[1] in variant_map and variant_map[word1[1]] == word2[1] or
            word2[1] in variant_map and variant_map[word2[1]] == word1[1]):
            return True
    
    return False

def find_variant_duplicates(input_file, output_file):
    """
    從重碼詞檔案中找出因繁簡/異體/混用造成的重碼
    """
    variant_duplicates = []
    
    with open(input_file, 'r', encoding='utf-16') as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            
            parts = line.split('\t')
            if len(parts) < 2:
                continue
            
            code = parts[0]
            words = parts[1].split()
            
            # 檢查這組詞語中是否有繁簡/異體關係
            found_pairs = []
            for i in range(len(words)):
                for j in range(i + 1, len(words)):
                    if is_variant_related(words[i], words[j]):
                        found_pairs.append((words[i], words[j]))
            
            if found_pairs:
                variant_duplicates.append((code, words, found_pairs))
    
    # 輸出結果
    with open(output_file, 'w', encoding='utf-16') as f:
        f.write('\ufeff')
